    <h2>
			<span>What is TransBASE?</span></h2>
    <p>
        <span>TransBASE is being developed to inform a comprehensive 
			approach to understanding health impacts of transportation systems, including safety, access, physical activity, air and noise 
			quality, and health disparities. TransBASE is part of a larger SFDPH-PHES effort to improve city services through San Francisco's 
			Open Data Policy. The goal of open data initiatives is to keep the San Francisco community informed, connected, and engaged with 
			government. To that end, SFDPH-PHES is working to improve the content and accessibility of TransBASE in coordination with City 
			agencies and community partners so that it can help address transportation system safety, sustainability, community health, and 
			equity in San Francisco. The long-term goal is for TransBase to serve as the central data repository for all public health-related 
			transportation data; to be a free and open data resource for the general public to use; and to support inter-agency collaboration, 
			data standards, and data sharing within San Francisco.&nbsp;</span>
    </p>
    <h2>
			<span>How is TransBASE different than DataSF?</span></h2>
    <p>
        <span>Although TransBASE contains some data that can be 
			found on DataSF, it seeks to create a relational database system where pedestrian and bicycle collision and injury data is 
			integrated with existing environmental (including but not limited to transportation factors) and socio-demographic data. 
			TransBASE is specialized analytical tool developed to support pedestrian safety initiatives, as well as other transportation 
			and health initiatives. TransBASE differs in its advanced analytical nature from DataSF, which serves as the City's open 
			governmental portal. SFDPH will use DataSF to facilitate a synergistic effort with San Francisco's Open Data Policy.&nbsp;</span>
    </p>
    <h2>
			<span>I am looking for xyz dataset but can't find it on TransBASE, why not?</span></h2>
    <p>
        <span>One of SFDPH's goals is to use TransBASE to understand city agencies' data collection efforts and how that data can 
			be used to better inform city policy. An additional goal is to identify gaps within the existing data and make recommendations 
			regarding new data to advance City initiatives, particularly those related to the impact of transportation systems on public 
			health. Utilization of TransBASE will hopefully allow agencies to better understand what data is available on a citywide scale 
			and how they might contribute additional data to the system.&nbsp;</span>
    </p>
    <h2>
			<span>Have you considered Open Street Map as an alternative to Department of Public Work's street centerline file which has known errors?</span></h2>
    <p>
        <span>Creating a new street centerline file for San Francisco is beyond the scope of this project. If the Department of Technology, DPW, 
			or another agency determines it is necessary to create a new GIS street centerline file, TransBASE will be adapted to use this new dataset 
			as its street and intersection network base.&nbsp;</span>
    </p>
